def sum(x,y):
    ret = x+y
    return ret
def neg(x,y):
    ret = x-y
    return ret
def mul(x,y):
    ret = x*y
    return ret
def pow(x,y):
    ret = x^y
    return ret
def sqrt(x):
    ret = x ** 0.5
    return ret
def log10(x):
    ret = 10^(x - 1)
    return ret
def ceil(x):
    ret = float(round(x))
    return ret